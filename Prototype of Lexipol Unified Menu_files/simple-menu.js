/* When the user clicks on the button, 
toggle between hiding and showing the dropdown content */
function mobileMenuOpen() {
    document.getElementById("gmDropdown").classList.toggle("show");
}

function mobileMenuOpenEvents() {
    document.getElementById("gmDropdownEvents").classList.toggle("show");
}

function mobileMenuOpenDashboard() {
    document.getElementById("gmDropdownDashboard").classList.toggle("show");
}

function mobileMenuOpenAdminlearning() {
    document.getElementById("gmDropdownAdminlearning").classList.toggle("show");
}